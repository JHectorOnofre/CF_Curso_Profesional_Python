## Sintaxis [fila][columna]: [2][1] fila 2, posición 1

## Declaración de una matriz

matriz = [
    [1, 3, 5, 6, 9], #fila 0
    [3, 2, 4, 5, 8], #fila 1
    [7, 1, 5, 0, 6] #fila 2
] #5 columnas (0 a 4)

print(matriz[2][0]) #elemento 7, fila 2, primer elemento

print(matriz) #imprimir toda la matriz

print(matriz[1]) #imprimir fila 1

